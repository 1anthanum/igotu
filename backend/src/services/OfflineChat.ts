// ── 主逻辑 ────────────────────────────────────────────────

function matchRule(message: string): KeywordRule | null {
  const lowerMsg = message.toLowerCase();
  const matched = RULES
    .filter((rule) => rule.keywords.some((kw) => lowerMsg.includes(kw)))
    .sort((a, b) => b.priority - a.priority);
  return matched.length > 0 ? matched[0] : null;
}

function formatResponse(resp: OfflineResponse): string {
  const choicesBlock = resp.choices.map((c) => c).join('\n');
  return `${resp.text}\n\n\`\`\`choices\n${choicesBlock}\n\`\`\``;
}

/**
 * 生成离线回复。
 * @param message 用户消息
 * @param isFirstMessage 是否是新对话的第一条消息
 */
export function generateOfflineResponse(message: string, isFirstMessage: boolean = false): string {
  // 新对话的首条：使用开场白
  if (isFirstMessage || !message.trim()) {
    return formatResponse(GREETING);
  }

  // 关键词匹配
  const rule = matchRule(message);
  if (rule) {
    return formatResponse(rule.response);
  }

  // 通用回退（随机选一个）
  const fallback = GENERIC_RESPONSES[Math.floor(Math.random() * GENERIC_RESPONSES.length)];
  return formatResponse(fallback);
}
